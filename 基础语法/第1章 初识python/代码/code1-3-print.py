"""
    变量、变量的赋值
"""
# 1.声明变量
# 变量名为year ，值为2024，=代表赋值   =号右边的值赋值给左边的变量
year = 2024  # 年份
month = 2
day = 20
week = "一"
weather = "晴"
temp = 19.5

print(2024) # 输出：2024
print(year) # 引入变量year，输出：2024
print(year , month , day) # 引入多个变量，输出：2024 2 20

print("我是mia")

# 参数1：year
#  参数2："年，我要减肥"
# 参数3：sep:设置打印多个内容的分隔符，sep=""  不要分隔符
# 参数4：end:设置print执行结束后的操作  end="\n\n"  内容打印输出之后，执行 \n 换一行
print(year , "年，我要减肥" ,  sep="/" ,  end="\n")
print("***")

print(year, "年，我要读100本书", sep="", end="\n\n")

print(year, "年，我要去10个城市旅游", sep="", end="\n\n")

# 2.占位输出： %d  代表整数（位数不限制） ，  %02d 代表两位整数  ， %s 代表字符串 ， %.1f 代表小数（小数点后一位）
# % 代表占位符
print("今天是 %d 年 %02d 月 %d 日,星期%s,天气%s，温度%.1f"  % (year, month, day, week, weather, temp))

# 基本的 %d 格式化
print("日期: %d月%d日" % (3, 5))  # 输出: 日期: 3月5日
# 使用 %02d 进行补零
print("日期: %02d月%02d日" % (3, 5))  # 输出: 日期: 03月05日
# 处理两位数
print("日期: %02d月%02d日" % (12, 31))  # 输出: 日期: 12月31日
# 处理更大的数字
print("数字: %02d" % 123)  # 输出: 数字: 123 (宽度限制只在数字不足时生效)