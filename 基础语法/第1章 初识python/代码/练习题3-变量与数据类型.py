"""
练习题3：变量与数据类型

题目：创建以下变量并输出：
- 学校名称（字符串）："北京大学"
- 建校年份（整数）：1898
- 占地面积（浮点数）：274.5（单位：公顷）
- 是否为985高校（可以用字符串表示）："是"

要求使用一个print语句输出所有信息，格式为：
北京大学建立于1898年，占地面积274.5公顷，是985高校。

考查知识点：变量赋值、不同数据类型、字符串拼接
"""

# 练习题3：变量与数据类型
# 创建不同类型的变量并格式化输出

# 定义学校相关信息变量
# 当变量名包含有多个单词时，使用下划线连接
school_name = "北京大学"        # 字符串类型
founding_year = 1898          # 整数类型
area = 274.5                  # 浮点数类型
# 是否、判断变量  变量名 is_xxx
is_985 = "是"                 # 字符串类型

# 使用字符串拼接输出完整信息
print("%s建立于%02d年，占地面积%.1f公顷，%s985高校。" % (school_name , founding_year, area, is_985) )
# str(xxx) : 将其他类型的变量值，转换为字符串类型
print(school_name + "建立于" + str(founding_year) + "年，占地面积" + str(area) + "公顷，" + is_985 + "985高校。")