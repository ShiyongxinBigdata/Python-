"""
练习题5：字符串格式化综合应用

题目：制作一个简单的天气播报程序：
- 使用input获取：城市名称、日期、天气状况、最高温度、最低温度
- 使用%格式化输出天气播报，格式如下：
================天气播报================
城市：北京
日期：2024年03月15日
天气：晴转多云
温度：最高15℃，最低3℃
====================================

要求：
- 温度需要转换为整数类型
- 使用%格式化方式输出
- 注意对齐和美观

考查知识点：input函数、类型转换、%字符串格式化、程序设计思维
"""

# 练习题5：字符串格式化综合应用
# 天气播报程序

# 获取用户输入
city = input("请输入城市名称：")
date = input("请输入日期（如：2024年03月15日）：")
weather = input("请输入天气状况：")
max_temp = input("请输入最高温度：")
min_temp = input("请输入最低温度：")

# 类型转换
max_temp = int(max_temp)
min_temp = int(min_temp)

# 使用%格式化输出天气播报
print("================天气播报================")
print("城市：%s" % city)
print("日期：%s" % date)
print("天气：%s" % weather)
print("温度：最高%d℃，最低%d℃" % (max_temp, min_temp))
print("====================================") 