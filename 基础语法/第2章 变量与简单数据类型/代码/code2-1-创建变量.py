"""
第2章 变量与简单数据类型 - 创建变量
功能：演示如何创建变量并进行简单的数学计算
场景：买苹果计算总价
"""

# 任务：买苹果
# 创建变量存储苹果的单价（每斤价格）
price = 10.5  # 浮点数类型，表示每斤10.5元

# 创建变量存储购买的重量
weight = 7.5  # 浮点数类型，表示购买7.5斤

# 计算总消费金额：单价 × 重量
money = price * weight  # 变量money存储计算结果

# 使用%格式化输出结果，%.2f表示保留2位小数
print("总共消费了 %.2f 元" % money)  # 输出：总共消费了 78.75 元

