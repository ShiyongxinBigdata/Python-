"""
第2章 变量与简单数据类型 - 支付宝余额
功能：演示变量的连续修改操作，模拟支付宝余额变化
场景：支付宝账户的收入、支出和清零操作
"""

# 支付宝余额变化模拟
# 初始余额设置为100元
total = 100  # 初始余额
print('支付宝余额：%f' % total)  # 输出：支付宝余额：100.000000

# 收入操作：余额增加10.5元（可能是转账收入）
total = total + 10.5  # 等价于 total += 10.5，余额变为110.5
print('支付宝余额：%f' % total)  # 输出：支付宝余额：110.500000

# 支出操作：余额减少20元（可能是购物消费）
total = total - 20  # 等价于 total -= 20，余额变为90.5
print('支付宝余额：%f' % total)  # 输出：支付宝余额：90.500000

# 清零操作：余额减去自身，变为0
total = total - total  # 等价于 total = 0，余额变为0
print('支付宝余额：%f' % total)  # 输出：支付宝余额：0.000000