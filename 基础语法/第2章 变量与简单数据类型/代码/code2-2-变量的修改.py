# 以下是注释掉的示例代码，演示变量修改的基本概念
# year = 2023        # 第一次赋值
# print(year)        # 输出：2023
# year = 2024        # 重新赋值，覆盖原来的值
# print(year)        # 输出：2024
#
# money = 10         # 整数类型
# money = '10元'     # 重新赋值为字符串类型（Python允许改变变量类型）
# print(money)       # 输出：10元

# 实际任务：超市打折活动
# 创建变量存储商品单价
price = 10.5  # 浮点数，每斤10.5元

# 创建变量存储购买重量
weight = 7.5  # 浮点数，购买7.5斤

# 计算原始总价
money = price * weight  # money = 78.75

# 修改money变量的值：应用5元优惠券
money = money - 5  # 等价于 money -= 5，最终money = 73.75

# 输出最终消费金额
print("总共消费了 %.2f 元" % money)  # 输出：总共消费了 73.75 元