"""
第2章 变量与简单数据类型 - 字符串
功能：演示字符串的创建方式、拼接和乘法操作
重点：单引号、双引号、三引号、转义字符、字符串运算
"""

# 创建字符串的不同方式
# 方式1：使用单引号创建字符串
s1 = 'hello'
print(s1)  # 输出：hello

# 方式2：使用双引号创建字符串（与单引号效果相同）
s2 = "hello"
print(s2)  # 输出：hello

# 方式3：使用三引号创建多行字符串
s3 = '''2024
hello
world'''
print(s3)  # 输出：三行内容，保持原有格式

s3 = '''SELECT
    *
FROM
    t_student
'''
print(s3)

# 在字符串中包含单引号的方法：使用双引号包围
s4 = "It's a hat"  # 字符串内部包含单引号
print(s4)  # 输出：It's a hat
s4 = "我是'马百祥'"
print(s4)
s4 = '我是"马百祥"'
print(s4)
# s4 = "我是"马百祥""   错误

# 使用转义字符在字符串中包含引号，正则表达式
s5 = '1234\'\"6666'  # \'表示单引号，\"表示双引号
print(s5)  # 输出：1234'"6666

# 字符串拼接(默认只支持字符串)
print('-------字符串拼接-------')
# 使用+号连接多个字符串
print(s1 + s4 + s5)  # 输出：helloIt's a hat1234'"6666

# 注意：字符串与数字不能直接相加
n = 5
m = '6'
# print(s1 + n)  # 这行代码会报错：TypeError
print(int(m) + n)  # 必须先转换类型，再计算

# 字符串乘法
print('-----字符串乘法-------')
# 字符串乘以整数，表示重复该字符串指定次数
print(s1 * 3)      # 输出：hellohello
print('@' * 20)    # 输出：@@@@@@@@@@@@@@@@@@@@（20个@符号）
print(3 * 'happy') # 输出：happyhappyhappy

print('=' * 20 , '操作开始' , '=' * 20)