"""
第2章 变量与简单数据类型 - 字符串索引
功能：演示字符串的索引访问、切片操作和字符串反转
重点：正向索引、负向索引、切片语法、步长参数
"""

# 创建字符串用于演示索引操作
# 字符串长度 : 字符串中包含的字符的个数
s = 'hello,world'  # 字符串长度为11，索引范围0-10

# 字符串索引访问（从0开始计数）
print(s[0])   # 输出：h（第1个字符，索引为0）
print(s[4])   # 输出：o（第5个字符，索引为4）
print(s[10]) # 输出：d
print(s[-1])  # 输出：d（最后一个字符，负索引-1表示倒数第1个）

# 字符串切片操作
# 语法：变量名[起始索引:结束索引+1:步数]
# 步数默认为1，可省略不写
# 起始索引默认为0，可省略不写  
# 结束索引默认为-1,可省略不写
print(s[0:4])  # 输出：hell（包头不包尾，取索引0到3的字符）
print(s[6:9])  # 输出：wor（取索引6到8的字符）

# 使用步长参数的切片示例
s2 = '123456789'  # 数字字符串，便于观察切片效果
       # 长度：9    下标范围：0~8
# 从索引0到8，步长为2（每隔一个字符取一个）
print(s2[0:9:2])  # 输出：13579（取奇数位置的字符）
# 省略起始和结束索引，步长为3
print(s2[::3])    # 输出：147（从头到尾，每隔2个字符取一个）

# 字符串反转操作
# 方法1：使用负步长反转（从最后一个字符开始，到第一个字符）
print(s2[-1:-10:-1])  # 输出：987654321（从索引-1到-9，步长-1）
# 方法2：更简洁的反转方法
print(s2[::-1])       # 输出：987654321（省略起始和结束索引，步长-1表示反转）