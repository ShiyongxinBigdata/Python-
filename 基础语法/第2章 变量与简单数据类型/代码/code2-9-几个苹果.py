"""
第2章 变量与简单数据类型 - 几个苹果
功能：演示变量的数学运算和字符串格式化输出
场景：计算苹果分配问题
"""

# 苹果分配问题
# 总共有10个苹果
total = 10  # 苹果总数

# 小a拿走的苹果数量
a = 2  # 小a拿走2个苹果

# 小b拿走的苹果数量  
b = 4  # 小b拿走4个苹果

# 计算小c可以拿走的苹果数量（剩余苹果数）
c = total - a - b  # c = 10 - 2 - 4 = 4个苹果

# 计算小a和小b一共拿走的苹果数量
d = a + b  # d = 2 + 4 = 6个苹果

# 使用%格式化输出结果
print('小a和小b一共拿走了 %d 个苹果' % d)  # 输出：小a和小b一共拿走了 6 个苹果
print('小c可以拿走 %d 个苹果' % c)        # 输出：小c可以拿走 4 个苹果