"""
第2章练习题2：变量修改与累计

题目：模拟银行存款变化
- 初始存款：1000元
- 第一次存入：500元
- 第二次取出：200元
- 第三次存入：300元
- 每次操作后都要输出当前余额

输出格式：
初始余额：1000.00元
存入500元后，余额：1500.00元
取出200元后，余额：1300.00元
存入300元后，余额：1600.00元

考查知识点：变量修改、累计运算、格式化输出
"""

# 银行存款变化模拟
# 初始存款
balance = 1000.0  # 初始余额
print("初始余额：%.2f元" % balance)

# 第一次存入500元
balance = balance + 500  # 等价于 balance += 500
print("存入500元后，余额：%.2f元" % balance)

# 第二次取出200元
balance = balance - 200  # 等价于 balance -= 200
print("取出200元后，余额：%.2f元" % balance)

# 第三次存入300元
balance = balance + 300  # 等价于 balance += 300
print("存入300元后，余额：%.2f元" % balance) 