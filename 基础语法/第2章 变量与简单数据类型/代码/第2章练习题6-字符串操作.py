"""
第2章练习题6：字符串操作

题目：个人信息字符串处理
- 创建姓名字符串：name = "李明"
- 创建爱好字符串：hobby = "编程"
- 创建学校字符串：school = "北京大学"
- 使用字符串拼接创建自我介绍
- 使用字符串乘法创建分隔线
- 创建包含单引号的句子

输出格式：
==========================================
大家好，我叫李明，我的爱好是编程，我在北京大学学习。
我说："今天天气真不错！"
==========================================

考查知识点：字符串创建、字符串拼接、字符串乘法、转义字符
"""

# 个人信息字符串处理
# 创建基本信息字符串
name = "李明"           # 姓名
hobby = "编程"          # 爱好
school = "北京大学"     # 学校

# 创建分隔线（字符串乘法）
separator = "=" * 42

# 字符串拼接创建自我介绍
introduction = "大家好，我叫" + name + "，我的爱好是" + hobby + "，我在" + school + "学习。"

# 包含引号的句子（使用转义字符或不同引号）
quote = "我说：\"今天天气真不错！\""

# 输出结果
print(separator)
print(introduction)
print(quote)
print(separator) 