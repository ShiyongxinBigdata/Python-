"""
第3章 运算符与表达式 - 赋值运算符
功能：演示基本赋值和复合赋值运算符的使用
重点：=赋值、+=自增、复合赋值运算符的简化写法
"""

# 基本赋值运算符
a = 2  # 将数值2赋值给变量a
print(a)  # 输出：2

# 复合赋值运算符：+=（加法赋值）
a += 2  # 等价于 a = a + 2，将a的值加上2后重新赋值给a
print(a)  # 输出：4

# 传统的赋值写法（与+=等价）
a = a + 2  # 明确写出：a的当前值加上2，然后赋值给a
print(a)  # 输出：6

# 说明：复合赋值运算符是传统赋值的简化写法
# 其他常用的复合赋值运算符：
# -= (减法赋值)、*= (乘法赋值)、/= (除法赋值)、//= (整除赋值)、%= (取余赋值)、**= (幂运算赋值)