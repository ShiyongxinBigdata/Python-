"""
第3章 运算符与表达式 - 比较运算符
功能：演示各种比较运算符的使用和特殊情况
重点：==、!=、>、<、>=、<=、链式比较、字符串比较
"""

# 不等于运算符 !=
print(3 != 3)  # 输出：False，3不等于3是假的

# 等于运算符 ==
print(3 == 2)  # 输出：False，3等于2是假的

# 大于等于运算符 >=
print(3 >= 2)  # 输出：True，3大于等于2是真的

# 小于等于运算符 <=
print(3 <= 3)  # 输出：True，3小于等于3是真的

# 不同类型数值的比较（整数与浮点数）
print(3.0 == 3)  # 输出：True，浮点数3.0等于整数3

# 布尔值的比较
print(True == False)  # 输出：False，True不等于False

# 字符串的比较运算（按ASCII码值逐字符比较）
print('hello' < 'hell')  # 输出：False，'hello'的第5个字符'o'与'hell'比较时，'hell'没有第5个字符

# 链式比较运算（Python特有的语法糖）
print(1 < 2 < 3)  # 输出：True，等价于 (1 < 2) and (2 < 3)

# 链式比较的等价写法
print(1 < 2 and 2 < 3)  # 输出：True，明确的逻辑与运算

# 复杂的链式比较
print('y' < 'x' == False)  # 输出：True，等价于 ('y' < 'x') and ('x' == False)

# 上述链式比较的等价写法
print('y' < 'x' and 'x' == False)  # 输出：False，注意：这里结果不同，因为运算优先级不同