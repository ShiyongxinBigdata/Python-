"""
第3章练习题2：赋值运算符练习

题目：游戏积分系统
- 初始积分：100分
- 完成任务1：积分增加50分（使用+=）
- 购买道具：积分减少30分（使用-=）
- 获得双倍奖励：积分翻倍（使用*=）
- 分享给好友：积分除以2（使用/=）
- 每次操作后显示当前积分

输出格式：
初始积分：100分
完成任务1后积分：150分
购买道具后积分：120分
获得双倍奖励后积分：240分
分享给好友后积分：120.0分

考查知识点：各种复合赋值运算符（+=、-=、*=、/=等）
"""

# 游戏积分系统
# 初始积分
score = 100
print("初始积分：%d分" % score)

# 完成任务1：积分增加50分（使用+=）
score += 50  # 等价于 score = score + 50
print("完成任务1后积分：%d分" % score)

# 购买道具：积分减少30分（使用-=）
score -= 30  # 等价于 score = score - 30
print("购买道具后积分：%d分" % score)

# 获得双倍奖励：积分翻倍（使用*=）
score *= 2   # 等价于 score = score * 2
print("获得双倍奖励后积分：%d分" % score)

# 分享给好友：积分除以2（使用/=）
score /= 2   # 等价于 score = score / 2，注意结果变为浮点数
print("分享给好友后积分：%.1f分" % score)

# 其他复合赋值运算符演示
print("\n=== 其他复合赋值运算符演示 ===")

# 整除赋值 //=
points = 100
points //= 3  # 等价于 points = points // 3
print("整除赋值后：%d分" % points)

# 取余赋值 %=
remainder = 100
remainder %= 7  # 等价于 remainder = remainder % 7
print("取余赋值后：%d分" % remainder)

# 幂运算赋值 **=
base = 2
base **= 3  # 等价于 base = base ** 3
print("幂运算赋值后：%d分" % base) 