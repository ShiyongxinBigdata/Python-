"""
第2章练习题5：浮点数处理

题目：圆形面积计算与取整
- 定义圆周率π = 3.14159
- 用户输入圆的半径
- 计算圆的面积（π * r²）
- 分别输出：原始面积、四舍五入到2位小数、向上取整、向下取整

输出格式：
请输入圆的半径：5.5
原始面积：94.985
四舍五入面积：94.99
向上取整面积：95
向下取整面积：94

考查知识点：浮点数运算、round()函数、math.ceil()、math.floor()
"""
import math   # 代码显示灰色的话，代表着当前代码没有被引用

# 定义变量
pi = 3.14159
r = float(input("请输入圆的半径："))
area = pi * r * r   #   pi * r ** 2
print("原始面积：%.3f" % area)
# 导包  : 在Python中，如果想使用第三方库的API代码，是需要使用import关键字导包的
# import 关键词的位置，一般放在Python文件的顶部
print("四舍五入面积：%.2f" % round(area, 2))
print("向上取整面积：%d" % math.ceil(area))
print("向下取整面积：%d" % math.floor(area))
